		,CASE 
			WHEN DATEDIFF(dd, A.B11C_BSAIK_BLDAT, B.B11E_BSAIK_BLDAT) <= 0 THEN '<= 0' 
			WHEN DATEDIFF(dd, A.B11C_BSAIK_BLDAT, B.B11E_BSAIK_BLDAT) BETWEEN 1 AND 33 THEN '1 - 33' 
			WHEN DATEDIFF(dd, A.B11C_BSAIK_BLDAT, B.B11E_BSAIK_BLDAT) BETWEEN 34 AND 60 THEN '34 - 60' 
			WHEN DATEDIFF(dd, A.B11C_BSAIK_BLDAT, B.B11E_BSAIK_BLDAT) BETWEEN 61 AND 90 THEN '61 - 90' 
