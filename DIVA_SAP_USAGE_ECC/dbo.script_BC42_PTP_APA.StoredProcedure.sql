			WHEN DATEDIFF(dd, A.B11C_BSAIK_BLDAT, B.B11E_BSAIK_BLDAT) BETWEEN 121 AND 180 THEN '121 - 180' 
			WHEN DATEDIFF(dd, A.B11C_BSAIK_BLDAT, B.B11E_BSAIK_BLDAT) BETWEEN 181 AND 360 THEN '181 - 360'
			ELSE '> 360'
		END ZF_PAY_MINUS_INV_BLDAT_BUCKET
