								ELSE BC41B_BSAIK_ZBD1T 
							 END
						  ELSE BC41B_BSAIK_ZBD1T 
					   END, BC41B_BSAIK_ZFBDT)			
				,CASE 
					WHEN COALESCE(BC41B_BSAIK_AUGBL, '') = '' THEN NULL 
