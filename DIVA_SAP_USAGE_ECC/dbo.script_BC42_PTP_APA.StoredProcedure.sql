-- Step 6 AP Ageing cube
-- Create a temp table with all month ends between @DATE1 and @DATE2
-- 06_01_2022 Thuan : Remove MANDT field (join)

	DECLARE @DATELOW date = (SELECT CAST(@DATE1 AS date))
	DECLARE @DATEHIGH date = (SELECT CAST(@DATE2 AS date))

	EXEC SP_DROPTABLE 'BC42_11_TT_SNAPSHOT_DATE'
	CREATE TABLE BC42_11_TT_SNAPSHOT_DATE (ZF_MNTH_END date)
	WHILE @DATELOW <= @DATEHIGH
	BEGIN 
		INSERT INTO BC42_11_TT_SNAPSHOT_DATE 
		SELECT EOMONTH(@DATELOW)
		SET @DATELOW = DATEADD(mm, 1, @DATELOW)
	END

-- add compay code, mandant and fiscal year variant
	EXEC SP_DROPTABLE 'BC42_12_TT_SNAPSHOT_BUKRS'
	SELECT T001_MANDT, T001_BUKRS, T001_PERIV, ZF_MNTH_END 
	INTO BC42_12_TT_SNAPSHOT_BUKRS 
	FROM A_T001
	INNER JOIN AM_SCOPE ON A_T001.T001_BUKRS = AM_SCOPE.SCOPE_CMPNY_CODE
	CROSS JOIN BC42_11_TT_SNAPSHOT_DATE
	ORDER BY
		T001_BUKRS, ZF_MNTH_END ASC

	EXEC SP_DROPTABLE 'BC42_14_IT_AP_AGEING'
	SELECT
		BC42_06_IT_PTP_INV.BSAIK_MANDT
		,BC42_06_IT_PTP_INV.BSAIK_BUKRS
		,BC42_06_IT_PTP_INV.BSAIK_GJAHR
		,BC42_06_IT_PTP_INV.BSAIK_BELNR
		,BC42_06_IT_PTP_INV.BSAIK_BUZEI
		,BC42_12_TT_SNAPSHOT_BUKRS.ZF_MNTH_END
		,BC42_06_IT_PTP_INV.BSAIK_AUGDT
		,BC42_06_IT_PTP_INV.BSAIK_LIFNR
		,BC42_06_IT_PTP_INV.ZF_BSAIK_ZBDXT_DUE_DATE
		,DATEDIFF(dd, BC42_06_IT_PTP_INV.ZF_BSAIK_ZBDXT_DUE_DATE, BC42_12_TT_SNAPSHOT_BUKRS.ZF_MNTH_END) ZF_BSAIK_ZBDXT_DIFF
		,BC42_06_IT_PTP_INV.BSAIK_BLDAT -- Document date
		,DATEDIFF(dd, BC42_06_IT_PTP_INV.BSAIK_BLDAT, BC42_12_TT_SNAPSHOT_BUKRS.ZF_MNTH_END) AS ZF_BLDAT_AGE_DIFF --Z_Age (document date)
		,BC42_06_IT_PTP_INV.BSAIK_ZFBDT --Baseline date
		,DATEDIFF(dd, BC42_06_IT_PTP_INV.BSAIK_ZFBDT, BC42_12_TT_SNAPSHOT_BUKRS.ZF_MNTH_END) AS ZF_ZFBDT_AGE_DIFF --Z_Age (baseline date)
		,BC42_06_IT_PTP_INV.BSAIK_BUDAT--Posting date
		,DATEDIFF(dd, BC42_06_IT_PTP_INV.BSAIK_BUDAT, BC42_12_TT_SNAPSHOT_BUKRS.ZF_MNTH_END) AS ZF_BUDAT_AGE_DIFF --Z_Age (posting date)
		,CASE 
			WHEN DATEDIFF(dd, BC42_06_IT_PTP_INV.ZF_BSAIK_ZBDXT_DUE_DATE, BC42_12_TT_SNAPSHOT_BUKRS.ZF_MNTH_END) <= 0 THEN '1. Current (<= 0)' 
			WHEN DATEDIFF(dd, BC42_06_IT_PTP_INV.ZF_BSAIK_ZBDXT_DUE_DATE, BC42_12_TT_SNAPSHOT_BUKRS.ZF_MNTH_END) BETWEEN 0  AND 30 THEN '2. <= 30' 
			WHEN DATEDIFF(dd, BC42_06_IT_PTP_INV.ZF_BSAIK_ZBDXT_DUE_DATE, BC42_12_TT_SNAPSHOT_BUKRS.ZF_MNTH_END) BETWEEN 30 AND 60 THEN '3. <= 60' 
			WHEN DATEDIFF(dd, BC42_06_IT_PTP_INV.ZF_BSAIK_ZBDXT_DUE_DATE, BC42_12_TT_SNAPSHOT_BUKRS.ZF_MNTH_END) BETWEEN 60 AND 90 THEN '4. <= 90' 
			WHEN DATEDIFF(dd, BC42_06_IT_PTP_INV.ZF_BSAIK_ZBDXT_DUE_DATE, BC42_12_TT_SNAPSHOT_BUKRS.ZF_MNTH_END) BETWEEN 90 AND 180 THEN '5. <= 180' 
			WHEN DATEDIFF(dd, BC42_06_IT_PTP_INV.ZF_BSAIK_ZBDXT_DUE_DATE, BC42_12_TT_SNAPSHOT_BUKRS.ZF_MNTH_END) BETWEEN 180 AND 360 THEN '6. <= 360'
			WHEN DATEDIFF(dd, BC42_06_IT_PTP_INV.ZF_BSAIK_ZBDXT_DUE_DATE, BC42_12_TT_SNAPSHOT_BUKRS.ZF_MNTH_END) > 360 THEN '7. > 360'
			ELSE 'No due date available'
		END ZF_BSAIK_ZBDXT_AGE_BUCKET
	   ,CASE 
			WHEN DATEDIFF(dd, BC42_06_IT_PTP_INV.BSAIK_BLDAT, BC42_12_TT_SNAPSHOT_BUKRS.ZF_MNTH_END) <= 0 THEN '1. Current (<= 0)' 
			WHEN DATEDIFF(dd, BC42_06_IT_PTP_INV.BSAIK_BLDAT, BC42_12_TT_SNAPSHOT_BUKRS.ZF_MNTH_END) BETWEEN 0 AND 30 THEN '2. <= 30'
			WHEN DATEDIFF(dd, BC42_06_IT_PTP_INV.BSAIK_BLDAT, BC42_12_TT_SNAPSHOT_BUKRS.ZF_MNTH_END) BETWEEN 30 AND 60 THEN '3. <= 60'
			WHEN DATEDIFF(dd, BC42_06_IT_PTP_INV.BSAIK_BLDAT, BC42_12_TT_SNAPSHOT_BUKRS.ZF_MNTH_END) BETWEEN 60 AND 90 THEN '4. <= 90'
			WHEN DATEDIFF(dd, BC42_06_IT_PTP_INV.BSAIK_BLDAT, BC42_12_TT_SNAPSHOT_BUKRS.ZF_MNTH_END) BETWEEN 90 AND 180 THEN '5. <= 180'
			WHEN DATEDIFF(dd, BC42_06_IT_PTP_INV.BSAIK_BLDAT, BC42_12_TT_SNAPSHOT_BUKRS.ZF_MNTH_END) BETWEEN 180 AND 360 THEN '6. <= 360'
			WHEN DATEDIFF(dd, BC42_06_IT_PTP_INV.BSAIK_BLDAT, BC42_12_TT_SNAPSHOT_BUKRS.ZF_MNTH_END) > 360 THEN '7. > 360'
			ELSE 'No document date available'
		END ZF_BLDAT_AGE_BUCKET
	   ,CASE 
			WHEN DATEDIFF(dd, BC42_06_IT_PTP_INV.BSAIK_ZFBDT, BC42_12_TT_SNAPSHOT_BUKRS.ZF_MNTH_END) <= 0 THEN '1. Current (<= 0)' 
			WHEN DATEDIFF(dd, BC42_06_IT_PTP_INV.BSAIK_ZFBDT, BC42_12_TT_SNAPSHOT_BUKRS.ZF_MNTH_END) BETWEEN 0 AND 30 THEN '2. <= 30' 
			WHEN DATEDIFF(dd, BC42_06_IT_PTP_INV.BSAIK_ZFBDT, BC42_12_TT_SNAPSHOT_BUKRS.ZF_MNTH_END) BETWEEN 30 AND 60 THEN '3. <= 60' 
			WHEN DATEDIFF(dd, BC42_06_IT_PTP_INV.BSAIK_ZFBDT, BC42_12_TT_SNAPSHOT_BUKRS.ZF_MNTH_END) BETWEEN 60 AND 90 THEN '4. <= 90' 
			WHEN DATEDIFF(dd, BC42_06_IT_PTP_INV.BSAIK_ZFBDT, BC42_12_TT_SNAPSHOT_BUKRS.ZF_MNTH_END) BETWEEN 90 AND 180 THEN '5. <= 180' 
			WHEN DATEDIFF(dd, BC42_06_IT_PTP_INV.BSAIK_ZFBDT, BC42_12_TT_SNAPSHOT_BUKRS.ZF_MNTH_END) BETWEEN 180 AND 360 THEN '6. <= 360'
			WHEN DATEDIFF(dd, BC42_06_IT_PTP_INV.BSAIK_ZFBDT, BC42_12_TT_SNAPSHOT_BUKRS.ZF_MNTH_END) > 360 THEN '7. > 360'
			ELSE 'No baseline date available'
		END ZF_ZFBDT_AGE_BUCKET
	   ,CASE 
			WHEN DATEDIFF(dd, BC42_06_IT_PTP_INV.BSAIK_BUDAT, BC42_12_TT_SNAPSHOT_BUKRS.ZF_MNTH_END) <= 0 THEN '1. Current (<= 0)' 
			WHEN DATEDIFF(dd, BC42_06_IT_PTP_INV.BSAIK_BUDAT, BC42_12_TT_SNAPSHOT_BUKRS.ZF_MNTH_END) BETWEEN 0 AND 30 THEN '2. <= 30' 
			WHEN DATEDIFF(dd, BC42_06_IT_PTP_INV.BSAIK_BUDAT, BC42_12_TT_SNAPSHOT_BUKRS.ZF_MNTH_END) BETWEEN 30 AND 60 THEN '3. <= 60' 
			WHEN DATEDIFF(dd, BC42_06_IT_PTP_INV.BSAIK_BUDAT, BC42_12_TT_SNAPSHOT_BUKRS.ZF_MNTH_END) BETWEEN 60 AND 90 THEN '4. <= 90' 
			WHEN DATEDIFF(dd, BC42_06_IT_PTP_INV.BSAIK_BUDAT, BC42_12_TT_SNAPSHOT_BUKRS.ZF_MNTH_END) BETWEEN 90 AND 180 THEN '5. <= 180' 
			WHEN DATEDIFF(dd, BC42_06_IT_PTP_INV.BSAIK_BUDAT, BC42_12_TT_SNAPSHOT_BUKRS.ZF_MNTH_END) BETWEEN 180 AND 360 THEN '6. <= 360'
			WHEN DATEDIFF(dd, BC42_06_IT_PTP_INV.BSAIK_BUDAT, BC42_12_TT_SNAPSHOT_BUKRS.ZF_MNTH_END) > 360 THEN '7. > 360'
			ELSE 'No posting date available'
		END ZF_BUDAT_AGE_BUCKET
	INTO BC42_14_IT_AP_AGEING
	FROM BC42_06_IT_PTP_INV
	LEFT JOIN BC42_12_TT_SNAPSHOT_BUKRS
	ON  BC42_06_IT_PTP_INV.BSAIK_BUKRS = BC42_12_TT_SNAPSHOT_BUKRS.T001_BUKRS
	WHERE (ISNULL(BC42_06_IT_PTP_INV.BSAIK_AUGDT, '') = '' AND BC42_06_IT_PTP_INV.BSAIK_BUDAT <= BC42_12_TT_SNAPSHOT_BUKRS.ZF_MNTH_END )
	--EXTRA SCRIPT
	OR (BC42_06_IT_PTP_INV.BSAIK_AUGDT > BC42_12_TT_SNAPSHOT_BUKRS.ZF_MNTH_END  AND BC42_06_IT_PTP_INV.BSAIK_BUDAT <= BC42_12_TT_SNAPSHOT_BUKRS.ZF_MNTH_END )


