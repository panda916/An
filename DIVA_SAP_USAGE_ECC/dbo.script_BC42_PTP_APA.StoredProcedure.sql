			WHEN DATEDIFF(dd, A.B11C_BSAIK_BUDAT, B.B11E_BSAIK_BUDAT) BETWEEN 91 AND 120 THEN '91 - 120' 
			WHEN DATEDIFF(dd, A.B11C_BSAIK_BUDAT, B.B11E_BSAIK_BUDAT) BETWEEN 121 AND 180 THEN '121 - 180' 
			WHEN DATEDIFF(dd, A.B11C_BSAIK_BUDAT, B.B11E_BSAIK_BUDAT) BETWEEN 181 AND 360 THEN '181 - 360'
