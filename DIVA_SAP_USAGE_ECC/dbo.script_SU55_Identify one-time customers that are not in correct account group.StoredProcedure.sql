--Khoi script
-- Get the data from about AR transactions- only get the lines relate to one-time customer
EXEC SP_DROPTABLE 'SU55_02_XT_BSAID_TRANSACTION_ONE_TIME_CUSTOMER'

SELECT A.*,B04_BSEG_KTOSL AS B04B_BSEG_KTOSL,T030W_LTEXT,KNA1_NAME1,
		TSTCT_TTEXT,T001_BUTXT,B04_BKPF_USNAM,V_USERNAME_NAME_TEXT
INTO SU55_02_XT_BSAID_TRANSACTION_ONE_TIME_CUSTOMER
FROM B04_12_IT_BSAD_BSID_AR_ACC_SCH AS A
LEFT JOIN 
B04_11_IT_FIN_GL AS B
ON B.B04_BSEG_BUKRS=A.B04B_BSAID_BUKRS
	AND B.B04_BSEG_GJAHR=A.B04B_BSAID_GJAHR
	AND B.B04_BSEG_BELNR= A.B04B_BSAID_BELNR
	AND B.B04_BSEG_BUZEI=A.B04B_BSAID_BUZEI
LEFT JOIN A_T030W 
	ON B04_BSEG_KTOSL=T030W_KTOSL AND T030W_SPRAS='EN'
LEFT JOIN A_TSTCT
	ON A_TSTCT.TSTCT_TCODE=B04_BKPF_TCODE AND A_TSTCT.TSTCT_SPRSL='EN'
LEFT JOIN A_KNA1
	ON B04B_BSAID_KUNNR= KNA1_KUNNR 
LEFT JOIN A_T001
	ON T001_BUKRS=B04B_BSAID_BUKRS
LEFT JOIN A_V_USERNAME
	ON V_USERNAME_BNAME=B04_BKPF_USNAM
WHERE B04B_BSAID_KUNNR IN
(
	SELECT DISTINCT SU55_01_KNB1_KUNNR
	FROM SU55_01_XT_KNB1_KNA1_ASSOCIATE_ONETIME_CUS_AND_ACCT_GROUP
)

--Step 2: sales documents, we need to see some stats on those that are one time customers and a focus on graphs that help us to analyse the ship-to customer in roder to see 
--if there are lots of shippings to the smae customer (shouldn't be because it is supposed to be "one-time")

--Get the sale document relate to one-time customer

EXEC SP_DROPTABLE SU55_03_XT_VBAK_VBAP_ONE_TIME_CUSTOMER

SELECT B33_01_IT_SALE_DOCUMENTS.*,
	B34_LIKP_KUNNR  --delivery ship to
	,B34_ZF_KNA1_NAME1_SHIP_TO
INTO SU55_03_XT_VBAK_VBAP_ONE_TIME_CUSTOMER
FROM 
B33_01_IT_SALE_DOCUMENTS
--Add the ship to from delivery, if it has delivery
LEFT JOIN B34_01_IT_DELIVERY_DOCS
ON B33_VBAK_VBELN=B34_LIKP_VBELN AND B33_VBAP_POSNR=B34_LIPS_POSNR
WHERE B33_VBAK_KUNNR IN
(
	SELECT DISTINCT SU55_01_KNB1_KUNNR
	FROM SU55_01_XT_KNB1_KNA1_ASSOCIATE_ONETIME_CUS_AND_ACCT_GROUP
)

