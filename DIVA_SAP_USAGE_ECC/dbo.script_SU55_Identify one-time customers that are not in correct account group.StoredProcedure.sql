EXEC SP_UNNAME_FIELD 'B04_','SU55_02_XT_BSAID_TRANSACTION_ONE_TIME_CUSTOMER'
EXEC SP_UNNAME_FIELD 'B33_','SU55_03_XT_VBAK_VBAP_ONE_TIME_CUSTOMER'
EXEC SP_UNNAME_FIELD 'B34_','SU55_03_XT_VBAK_VBAP_ONE_TIME_CUSTOMER'
EXEC SP_UNNAME_FIELD 'B34_','SU55_04_XT_DELIVERY_ONE_TIME_CUSTOMER'


EXEC SP_RENAME_FIELD 'SU55_02_','SU55_02_XT_BSAID_TRANSACTION_ONE_TIME_CUSTOMER'
EXEC SP_RENAME_FIELD 'SU55_03_','SU55_03_XT_VBAK_VBAP_ONE_TIME_CUSTOMER'
EXEC SP_RENAME_FIELD 'SU55_04_','SU55_04_XT_DELIVERY_ONE_TIME_CUSTOMER'


