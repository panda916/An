INTO SU55_04_XT_DELIVERY_ONE_TIME_CUSTOMER
FROM B34_01_IT_DELIVERY_DOCS
INNER JOIN SU55_03_XT_VBAK_VBAP_ONE_TIME_CUSTOMER
ON B33_VBAK_VBELN=B34_LIKP_VBELN AND B33_VBAP_POSNR=B34_LIPS_POSNR


