CREATE PROCEDURE [dbo].[script_SU42_Identify movement types that allow for GR reversal after IR]
WITH EXECUTE AS CALLER

AS
--DYNAMIC_SCRIPT_START

---Check if any movement type allows for the reversal of GR after IR. 

EXEC SP_DROPTABLE SU42_01_RT_T156_XWSBR_ADD_DESC;
SELECT  DISTINCT 
		T156_BWART,
		T156_XWSBR,
		T156_BWART+'-'+T156HT_BTEXT AS T156_BWART_DESC,
		T156HT_BTEXT,
		IIF(T156_XWSBR='X','Yes','No') AS T156_XWSBR_DESC
INTO SU42_01_RT_T156_XWSBR_ADD_DESC
FROM A_T156
LEFT JOIN A_T156HT ON T156_BWART=T156HT_BWART AND T156HT_SPRAS='EN'


EXEC SP_RENAME_FIELD 'SU42_01_','SU42_01_RT_T156_XWSBR_ADD_DESC';

GO
