CREATE   FUNCTION [dbo].[GET_PARAM]
  (@value varchar(255))
  RETURNS varchar(255)
  WITH EXEC AS CALLER
  AS
  BEGIN
  DECLARE @result varchar(255)

    SELECT @result = GLOBALS_VALUE FROM [dbo].AM_GLOBALS WHERE [dbo].[AM_GLOBALS].[GLOBALS_PARAMETER] = @value
    RETURN @result
  END


GO
